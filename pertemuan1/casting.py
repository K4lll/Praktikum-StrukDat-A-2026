# CASTING
# when you want to specify variable data type

# integer
i_1 = int(1)
i_2 = int(1.0)
i_3 = int("1")

print(i_1, type(i_1))
print(i_2, type(i_2))
print(i_3, type(i_3))

# float
f_1 = float(1)
f_2 = float(1.0)
f_3 = float("1")
f_4 = float("1.2")

print(f_1, type(f_1))
print(f_2, type(f_2))
print(f_3, type(f_3))
print(f_4, type(f_4))

# string
s_1 = str("string")
s_2 = str(1)
s_3 = str(1.2)

print(s_1, type(s_1))
print(s_2, type(s_2))
print(s_3, type(s_3))