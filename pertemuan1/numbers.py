# Tipe data awal
x = 1      # int
y = 2.8    # float
z = 1j     # complex

# convert dari int ke float
a = float(x)

# convert dari float ke int
b = int(y)

# convert dari int ke complex
c = complex(x)

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))